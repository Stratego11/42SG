#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && src[i])
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main(void)
{
	char 	src[] = "Hello";
	char	dest[6];

	printf("Before copy: %s\n", src);
	ft_strncpy(dest, src,4);
	printf("Destination: %s", dest);
	return (0);
}
